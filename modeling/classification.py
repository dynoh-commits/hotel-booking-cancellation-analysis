# 3. 3개 군집 x 3개 모델 = 3행 3열 대형 서브플롯 틀 생성

fig, axes = plt.subplots(3, 3, figsize=(18, 14))
fig.suptitle('5-Fold CV Feature Importance: Boosting Trilogy (AdaBoost vs GBM vs XGBoost)', fontsize=22, fontweight='bold', y=0.98)

cluster_names = {
    0: "Cluster 0: Budget Personal",
    1: "Cluster 1: Long-stay Family/Groups",
    2: "Cluster 2: High-spending VIPs"
}

folds = 5
skf = StratifiedKFold(n_splits=folds, shuffle=True, random_state=42)


# 4. 5-Fold 교차 검증 기반 부스팅 피처 중요도 추출 및 시각화

for row_idx, cluster_id in enumerate(sorted(df['customer_cluster'].unique())):
    df_cluster = df[df['customer_cluster'] == cluster_id]

    X_c = df_cluster.drop(columns=['is_canceled', 'customer_cluster']).reset_index(drop=True)
    y_c = df_cluster['is_canceled'].reset_index(drop=True)
    features = X_c.columns

    # 각 폴드의 중요도를 모을 빈 리스트
    ada_folds_imp = []
    gbm_folds_imp = []
    xgb_folds_imp = []

    for train_idx, _ in skf.split(X_c, y_c):
        X_train, y_train = X_c.iloc[train_idx], y_c.iloc[train_idx]

        # 수업 PDF 규격 준수 하이퍼파라미터 모델 정의 (Ref: Slide Page 72, 113, 117)
        m_ada = AdaBoostClassifier(estimator=DecisionTreeClassifier(max_depth=1, random_state=42), n_estimators=100, random_state=42)
        m_gbm = GradientBoostingClassifier(n_estimators=100, max_depth=3, random_state=42)
        m_xgb = XGBClassifier(n_estimators=100, max_depth=3, random_state=42, eval_metric='logloss')

        m_ada.fit(X_train, y_train)
        m_gbm.fit(X_train, y_train)
        m_xgb.fit(X_train, y_train)

        ada_folds_imp.append(m_ada.feature_importances_)
        gbm_folds_imp.append(m_gbm.feature_importances_)
        xgb_folds_imp.append(m_xgb.feature_importances_)

    # 5개 폴드 결과의 산술 평균 계산
    ada_final_avg = np.mean(ada_folds_imp, axis=0)
    gbm_final_avg = np.mean(gbm_folds_imp, axis=0)
    xgb_final_avg = np.mean(xgb_folds_imp, axis=0)

    model_data_mappings = [
        ('AdaBoost (Stumps)', ada_final_avg, "Purples_r"),
        ('Gradient Boosting', gbm_final_avg, "Greens_r"),
        ('XGBoost (Regularized)', xgb_final_avg, "Reds_r")
    ]

    # 3개 열에 순서대로 데이터 바인딩 및 시본 그래프 작도
    for col_idx, (model_name, importance, color_palette) in enumerate(model_data_mappings):
        ax = axes[row_idx, col_idx]

        fi_df = pd.DataFrame({'Feature': features, 'Importance': importance}).sort_values(by='Importance', ascending=False).head(5)

        sns.barplot(x='Importance', y='Feature', data=fi_df, ax=ax, palette=color_palette)

        # 첫 번째 행에만 상단 모델 타이틀 부여
        if row_idx == 0:
            ax.set_title(f' {model_name}', fontsize=14, fontweight='bold', pad=10)

