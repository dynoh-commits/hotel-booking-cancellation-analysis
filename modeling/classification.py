import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.cluster import KMeans
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import StratifiedKFold
from sklearn.ensemble import BaggingClassifier, RandomForestClassifier
from sklearn.tree import DecisionTreeClassifier
import warnings

warnings.filterwarnings('ignore')



sns.set_theme(style="whitegrid")
plt.rcParams['font.family'] = 'sans-serif'
plt.rcParams['axes.unicode_minus'] = False


# 2. 데이터 로드 및 K-Means 클러스터링

df = pd.read_csv("hotel_bookings_preprocessed.csv")

cluster_cols = ['adr', 'stays_in_weekend_nights', 'stays_in_week_nights', 'total_of_special_requests']
scaler = StandardScaler()
X_scaled = scaler.fit_transform(df[cluster_cols])

kmeans = KMeans(n_clusters=3, random_state=42)
df['customer_cluster'] = kmeans.fit_predict(X_scaled)


# 3. 3개 군집용 3행 2열(총 6개) 대형 시본 도표 틀 생성

fig, axes = plt.subplots(3, 2, figsize=(15, 14))
fig.suptitle('5-Fold CV Feature Importance: Pure Bagging vs Random Forest', fontsize=20, fontweight='bold', y=0.98)

# PPT 스토리텔링용 군집 매핑 이름
cluster_names = {
    0: "Cluster 0: Budget Personal Travelers",
    1: "Cluster 1: Long-stay Family/Groups",
    2: "Cluster 2: High-spending VIPs"
}

folds = 5
skf = StratifiedKFold(n_splits=folds, shuffle=True, random_state=42)


# 4. 5-Fold 교차 검증 기반 중요도 추출 및 Seaborn 매핑

for row_idx, cluster_id in enumerate(sorted(df['customer_cluster'].unique())):
    df_cluster = df[df['customer_cluster'] == cluster_id]

    X_c = df_cluster.drop(columns=['is_canceled', 'customer_cluster']).reset_index(drop=True)
    y_c = df_cluster['is_canceled'].reset_index(drop=True)
    features = X_c.columns

    bagging_folds_imp = []
    rf_folds_imp = []

    # 5-Fold 루프 안에서 각 시험 라운드의 피처 중요도를 누적
    for train_idx, _ in skf.split(X_c, y_c):
        X_train, y_train = X_c.iloc[train_idx], y_c.iloc[train_idx]

        # 수업 PDF 규격 모델 설정 (Ref: Slide Page 26, 42, 43)
        m_bagging = BaggingClassifier(
            estimator=DecisionTreeClassifier(max_depth=7, random_state=42),
            n_estimators=100, random_state=42, n_jobs=-1
        )
        m_rf = RandomForestClassifier(n_estimators=100, max_depth=7, random_state=42)

        m_bagging.fit(X_train, y_train)
        m_rf.fit(X_train, y_train)

        # 중요도 저장 (Bagging은 100개 트리의 피처 중요도 평균 산출)
        bagging_fi = np.mean([tree.feature_importances_ for tree in m_bagging.estimators_], axis=0)
        bagging_folds_imp.append(bagging_fi)
        rf_folds_imp.append(m_rf.feature_importances_)

    # 5회 교차 검증의 최종 평균 스코어 계산
    bagging_final_avg = np.mean(bagging_folds_imp, axis=0)
    rf_final_avg = np.mean(rf_folds_imp, axis=0)

    # --- 시각화를 위한 Seaborn 데이터프레임 변환 파트 ---
    # 왼쪽 열: Pure Bagging Top 5
    df_bagging = pd.DataFrame({'Feature': features, 'Importance': bagging_final_avg}).sort_values(by='Importance', ascending=False).head(5)
    ax_left = axes[row_idx, 0]
    sns.barplot(x='Importance', y='Feature', data=df_bagging, ax=ax_left, palette="Blues_r")

    # 오른쪽 열: Random Forest Top 5
    df_rf = pd.DataFrame({'Feature': features, 'Importance': rf_final_avg}).sort_values(by='Importance', ascending=False).head(5)
    ax_right = axes[row_idx, 1]
    sns.barplot(x='Importance', y='Feature', data=df_rf, ax=ax_right, palette="Oranges_r")

    # --- 도표 세부 디자인 및 축 레이벨링 규칙 설정 ---
    if row_idx == 0:
        ax_left.set_title('Pure Bagging (Decision Trees)', fontsize=13, fontweight='bold', pad=10)
        ax_right.set_title(' Random Forest (Feature Subsets)', fontsize=13, fontweight='bold', pad=10)

    # 각 군집 행의 대표 이름을 Y축 바깥에 배치
    ax_left.set_ylabel(f"{cluster_names[cluster_id]}", fontsize=12, fontweight='bold', labelpad=15)
    ax_right.set_ylabel('') # 우측 그래프는 Y축 라벨 중복 제거

    ax_left.set_xlabel('Mean Importance Score', fontsize=10)
    ax_right.set_xlabel('Mean Importance Score', fontsize=10)

# 간격 최적화 후 도표 띄우기
plt.tight_layout(rect=[0, 0, 1, 0.95])
plt.show()

